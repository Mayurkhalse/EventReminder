-- --------------------------------------------------------
--
-- Purpose: Defines the database schema for the Event Reminder Application.
-- This script is designed for a MySQL database and includes tables for
-- users, events, and AI-pending events, establishing clear relationships
-- and constraints.
--
-- --------------------------------------------------------

--
-- Database: `event_reminder`
--
CREATE DATABASE IF NOT EXISTS `event_reminder` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE `event_reminder`;

-- --------------------------------------------------------

--
-- Table structure for table `users`
--
-- Purpose: Stores user account information, including credentials and roles.
-- The `role` column distinguishes between standard 'user' and 'admin'.
--

CREATE TABLE `users` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `name` VARCHAR(100) NOT NULL,
  `email` VARCHAR(100) NOT NULL UNIQUE,
  `password` VARCHAR(255) NOT NULL,
  `role` ENUM('user', 'admin') NOT NULL DEFAULT 'user',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

