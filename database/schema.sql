--
-- Table structure for table `events`
--
-- Purpose: Stores all personal and global events.
-- A NULL `user_id` indicates a 'global' event created by an admin.
-- Reminder flags (`reminder1_sent`, `reminder2_sent`) track email notifications.
--

CREATE TABLE `events` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `user_id` INT NULL,
  `title` VARCHAR(255) NOT NULL,
  `description` TEXT,
  `event_date` DATETIME NOT NULL,
  `reminder1_date` DATETIME NOT NULL,
  `reminder2_date` DATETIME NOT NULL,
  `reminder1_sent` BOOLEAN DEFAULT FALSE,
  `reminder2_sent` BOOLEAN DEFAULT FALSE,
  `is_global` BOOLEAN DEFAULT FALSE,
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


-- --------------------------------------------------------

--
-- Table structure for table `ai_pending_events`
--
-- Purpose: Acts as a staging area for events generated by the AI PDF parser.
-- These events are not visible to users until an admin reviews and approves them,
-- at which point they are moved to the main `events` table.
--

CREATE TABLE `ai_pending_events` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `title` VARCHAR(255) NOT NULL,
  `description` TEXT,
  `event_date` DATETIME NOT NULL,
  `status` ENUM('pending', 'approved', 'rejected') NOT NULL DEFAULT 'pending',
  `source_file` VARCHAR(255) NOT NULL,
  `extracted_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

COMMIT;
